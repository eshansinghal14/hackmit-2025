// Hide loading screen after app initialization
const hideLoadingScreen = () => {
  const loadingElement = document.getElementById('loading')
  if (loadingElement) {
    loadingElement.style.opacity = '0'
    loadingElement.style.transition = 'opacity 0.5s ease-out'
    setTimeout(() => {
      loadingElement.remove()
    }, 500)
  }
}

// Initialize app
const initializeApp = async () => {
  try {
    console.log('🔄 Starting app initialization...')
    
    // Add any async initialization here
    await new Promise(resolve => setTimeout(resolve, 1000)) // Minimum loading time
    
    console.log('🔄 App initialized, hiding loading screen...')
    
    // Hide loading screen
    hideLoadingScreen()
    
    console.log('🚀 AI Whiteboard Tutor initialized successfully')
  } catch (error) {
    console.error('❌ Failed to initialize app:', error)
    hideLoadingScreen() // Hide loading screen even on error
  }
}

